#!/usr/bin/env python3
""" 5-sum_list.py """
from typing import List


def sum_list(input_list: List[float]) -> float:
    """
    Calculate the sum of a list of floats

    Args:
        input_list (list): a list of floats

    Returns: the sum of all the floats in the list
    """
    return sum(input_list)
